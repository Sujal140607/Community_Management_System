const mongoose = require("mongoose");

const areaSchema = new mongoose.Schema({
    areaName: {
        type: String,
        required: true,
    },
    city: {
        type: String,
        required: true,
    },
    district: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    pincode: {
        type: Number,
        required: true,
    },
    createdAt: {
        type: Date,
        
    }
})